    //Base64加密必须
    private String base64Encoder(byte[] bytes) {
        if (mEncoder == null) {
            mEncoder = new BASE64Encoder();
        }
        return mEncoder.encode(bytes);
    }

    private byte[] base64Decoder(String bytes) throws IOException {
        if (mDecoder == null) {
            mDecoder = new BASE64Decoder();
        }
        return mDecoder.decodeBuffer(bytes);
    }

